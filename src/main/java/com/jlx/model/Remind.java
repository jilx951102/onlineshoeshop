package com.jlx.model;
/**
 * 提醒的实体类
 * @author Administrator
 *
 */
public class Remind {
   private String remindId;
   
   private int addUser;
   
   private int addShoe;
   
   private int addComment;
   
   private int addOrder;

public String getRemindId() {
	return remindId;
}

public void setRemindId(String remindId) {
	this.remindId = remindId;
}

public int getAddUser() {
	return addUser;
}

public void setAddUser(int addUser) {
	this.addUser = addUser;
}

public int getAddShoe() {
	return addShoe;
}

public void setAddShoe(int addShoe) {
	this.addShoe = addShoe;
}

public int getAddComment() {
	return addComment;
}

public void setAddComment(int addComment) {
	this.addComment = addComment;
}

public int getAddOrder() {
	return addOrder;
}

public void setAddOrder(int addOrder) {
	this.addOrder = addOrder;
}
}
